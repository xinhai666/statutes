package com.hd.statutes.model.entity;


public class Contents {

  private Integer contentsId;
  private Integer contentsLevel;
  private String contentsName;
  private Integer contentsSerial;
  private Integer staId;
  private Integer conId;

  public Integer getContentsId() {
    return contentsId;
  }

  public void setContentsId(Integer contentsId) {
    this.contentsId = contentsId;
  }

  public Integer getContentsLevel() {
    return contentsLevel;
  }

  public void setContentsLevel(Integer contentsLevel) {
    this.contentsLevel = contentsLevel;
  }

  public String getContentsName() {
    return contentsName;
  }

  public void setContentsName(String contentsName) {
    this.contentsName = contentsName;
  }

  public Integer getContentsSerial() {
    return contentsSerial;
  }

  public void setContentsSerial(Integer contentsSerial) {
    this.contentsSerial = contentsSerial;
  }

  public Integer getStaId() {
    return staId;
  }

  public void setStaId(Integer staId) {
    this.staId = staId;
  }

  public Integer getConId() {
    return conId;
  }

  public void setConId(Integer conId) {
    this.conId = conId;
  }
}
