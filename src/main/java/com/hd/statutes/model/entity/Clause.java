package com.hd.statutes.model.entity;


public class Clause {

  private Integer clauseId;
  private Integer staId;
  private Integer conId;
  private String clauseNumber;
  private String clauseAnothername;
  private String clauseVersion;
  private String clauseTitle;
  private String clauseContent;
  private String clauseAntistop;


  public Integer getClauseId() {
    return clauseId;
  }

  public void setClauseId(Integer clauseId) {
    this.clauseId = clauseId;
  }


  public Integer getStaId() {
    return staId;
  }

  public void setStaId(Integer staId) {
    this.staId = staId;
  }


  public Integer getConId() {
    return conId;
  }

  public void setConId(Integer conId) {
    this.conId = conId;
  }


  public String getClauseNumber() {
    return clauseNumber;
  }

  public void setClauseNumber(String clauseNumber) {
    this.clauseNumber = clauseNumber;
  }


  public String getClauseAnothername() {
    return clauseAnothername;
  }

  public void setClauseAnothername(String clauseAnothername) {
    this.clauseAnothername = clauseAnothername;
  }


  public String getClauseVersion() {
    return clauseVersion;
  }

  public void setClauseVersion(String clauseVersion) {
    this.clauseVersion = clauseVersion;
  }


  public String getClauseTitle() {
    return clauseTitle;
  }

  public void setClauseTitle(String clauseTitle) {
    this.clauseTitle = clauseTitle;
  }


  public String getClauseContent() {
    return clauseContent;
  }

  public void setClauseContent(String clauseContent) {
    this.clauseContent = clauseContent;
  }


  public String getClauseAntistop() {
    return clauseAntistop;
  }

  public void setClauseAntistop(String clauseAntistop) {
    this.clauseAntistop = clauseAntistop;
  }

}
