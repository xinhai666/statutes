package com.hd.statutes.model.entity;


public class Sites {

  private Integer sitesId;
  private Integer sitesStatus;
  private String sitesTitle;
  private String sitesOrg;
  private String sitesLogo;
  private String sitesEmail;
  private Integer sitesLanguage;


  public Integer getSitesId() {
    return sitesId;
  }

  public void setSitesId(Integer sitesId) {
    this.sitesId = sitesId;
  }


  public Integer getSitesStatus() {
    return sitesStatus;
  }

  public void setSitesStatus(Integer sitesStatus) {
    this.sitesStatus = sitesStatus;
  }


  public String getSitesTitle() {
    return sitesTitle;
  }

  public void setSitesTitle(String sitesTitle) {
    this.sitesTitle = sitesTitle;
  }


  public String getSitesOrg() {
    return sitesOrg;
  }

  public void setSitesOrg(String sitesOrg) {
    this.sitesOrg = sitesOrg;
  }


  public String getSitesLogo() {
    return sitesLogo;
  }

  public void setSitesLogo(String sitesLogo) {
    this.sitesLogo = sitesLogo;
  }


  public String getSitesEmail() {
    return sitesEmail;
  }

  public void setSitesEmail(String sitesEmail) {
    this.sitesEmail = sitesEmail;
  }


  public Integer getSitesLanguage() {
    return sitesLanguage;
  }

  public void setSitesLanguage(Integer sitesLanguage) {
    this.sitesLanguage = sitesLanguage;
  }

}
