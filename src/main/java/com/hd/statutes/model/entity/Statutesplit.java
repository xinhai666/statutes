package com.hd.statutes.model.entity;


public class Statutesplit {

  private Integer statutesplitId;
  private Integer statpId;
  private String statutesplitName;


  public Integer getStatutesplitId() {
    return statutesplitId;
  }

  public void setStatutesplitId(Integer statutesplitId) {
    this.statutesplitId = statutesplitId;
  }


  public Integer getStatpId() {
    return statpId;
  }

  public void setStatpId(Integer statpId) {
    this.statpId = statpId;
  }


  public String getStatutesplitName() {
    return statutesplitName;
  }

  public void setStatutesplitName(String statutesplitName) {
    this.statutesplitName = statutesplitName;
  }

}
